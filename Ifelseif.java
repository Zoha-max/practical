class Ifelseif{
public static void main(String[] args){
String name = "Zoha";
if (name=="Meena")
  {
   System.out.println("Name is Meena");
  }
  else if (name=="Neha")
  {
   System.out.println("Name is Neha");
  }
  else if (name=="Ankit")
  {
   System.out.println("Name is Ankit");
  }
else
{
System.out.println(name);
}
}
}